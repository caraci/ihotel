package com.iHotel.model.Utility;

public class Prezzo {

	private double _quantità;
	private String _valuta;
	/**
	 * @return the _quantità
	 */
	public double get_quantità() {
		return _quantità;
	}
	/**
	 * @param _quantità the _quantità to set
	 */
	public void set_quantità(double _quantità) {
		this._quantità = _quantità;
	}
	/**
	 * @return the _valuta
	 */
	public String get_valuta() {
		return _valuta;
	}
	/**
	 * @param _valuta the _valuta to set
	 */
	public void set_valuta(String _valuta) {
		this._valuta = _valuta;
	}

}